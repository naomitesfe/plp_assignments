x = float(input("please input x"))
y = float(input("Please input y"))
z = input("please input operation")
if z == "+":
   print("x + y = ", x+y);
elif z == "-":
   print("x - y = ",x-y);
elif z=="/":
   print("x / y = ", x/y)
elif z=="*":
   print("x * y = ", x*y)
else:
   print("please choose from +,-,/,*");
